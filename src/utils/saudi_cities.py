def get_cities_list():
    """
    يرجع قائمة بأسماء المدن مرتبة أبجدياً
    """
    return sorted(SAUDI_CITIES.keys())

def get_city_info(city_name):
    """
    يرجع معلومات المدينة
    """
    return SAUDI_CITIES.get(city_name)

def get_cities_by_region(region):
    """
    يرجع قائمة المدن في منطقة معينة
    """
    return [city for city, info in SAUDI_CITIES.items() if info['region'] == region]

def get_regions_list():
    """
    يرجع قائمة المناطق الإدارية
    """
    regions = set(info['region'] for info in SAUDI_CITIES.values())
    return sorted(regions)
